#Afficher un String
puts "Hello, World!"

# .size donne le nombre de caractères
puts "Hello, World!".size

# [0] donne le caractère à la position 0, soit le 1er.
puts "Hello, World!"[0]
# [7] donne le caractère à la position 7, soit le 8e.
puts "Hello, World!"[7]
# [-1] donne le caractère à la position -1, soit le dernier.
puts "Hello, World!"[-1]

# .downcase transforme tous les caractères en minuscules, si possible.
puts "Hello, World!".downcase
# .upcase transforme tous les caractères en majuscules, si possible.
puts "Hello, World!".upcase
# .swapcase échange la casse de tous les caractères, si possible.
puts "Hello, World!".swapcase

# . reverse inverse l'ordre des caractères
puts "Hello, World!".reverse
